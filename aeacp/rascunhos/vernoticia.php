    <td colspan="6" rowspan="10" valign="top" bgcolor="eff6ee"><h4 class="v12b"><br>
      <img src="images/bolinha.gif" width="6" height="6" align="absmiddle">      Pa&iacute;ses Ibero-Americanos v&atilde;o implementar rastreabilidade</h4>
      <span class="style10">Representantes dos pa&iacute;ses que participaram na Espanha da XIII Confer&ecirc;ncia Ibero-Americana de Agricultura se comprometeram a implementar sistemas de rastreabilidade exigidos pela Europa nas importa&ccedil;&otilde;es de produtos aliment&iacute;cios, incluindo a carne bovina. A confer&ecirc;ncia reuniu em Santiago de Compostela, entre os dias 25 a 28 de setembro, ministros de Agricultura de 12 pa&iacute;ses da Am&eacute;rica Latina, al&eacute;m representantes dos governos da Espanha e de Portugal. </span>
      <p class="style10">Ao final da reuni&atilde;o, foi produzida a &ldquo;Declara&ccedil;&atilde;o de Santiago de Compostela&rdquo;, que dever&aacute; servir de base na estrutura&ccedil;&atilde;o dos modelos de rastreabilidade para os pa&iacute;ses ibero-americanos. &ldquo;Houve um movimento dos pa&iacute;ses com interesse em vender para o mercado europeu em implementar o sistema e os pa&iacute;ses compradores devem conceder um prazo para que os fornecedores se adaptem &agrave;s exig&ecirc;ncias&rdquo;, informou o secret&aacute;rio de Desenvolvimento Rural e Cooperativismo do Minist&eacute;rio da Agricultura, Pecu&aacute;ria e Abastecimento (Mapa), Marcio Portocarrero, que representou o ministro Roberto Rodrigues no encontro. </p>
      <p class="style10">Segundo o secret&aacute;rio, nas discuss&otilde;es sobre rastreabilidade os pa&iacute;ses demonstraram o desejo de que o sistema n&atilde;o se limite &agrave;s quest&otilde;es sanit&aacute;rias, mas se amplie para os setores ambiental, tecnol&oacute;gico e social. &ldquo;A preocupa&ccedil;&atilde;o &eacute; de que a exig&ecirc;ncia se transforme numa nova barreira sanit&aacute;ria, prejudicando os pa&iacute;ses exportadores&rdquo;, disse Portocarrero. Ele destacou que entre os pa&iacute;ses ibero-americanos, o Brasil &eacute; o que tem o modelo de rastreabilidade mais avan&ccedil;ado, apesar das dificuldades de implementa&ccedil;&atilde;o. &ldquo;Aprendemos com os erros. Hoje o produtor brasileiro tem consci&ecirc;ncia de que a rastreabilidae &eacute; uma vantagem competitiva&rdquo;. </p>
      <p class="style10">O secret&aacute;rio ressaltou ainda que com o trabalho desenvolvido pela Embrapa, vinculada ao Mapa, o Brasil det&eacute;m tecnologias que o coloca &agrave; frente de outros pa&iacute;ses ibero-americanos. &ldquo;O Brasil est&aacute; na vanguarda. Argentina, Chile e Uruguai j&aacute; experimentaram modelos de rastreabilidade, mas o Brasil lidera a tecnologia - se comparado com os pa&iacute;ses vizinhos&rdquo;, disse. </p>
      <p class="style10">O Brasil implantou a rastreabilidade em 2002. Desde ent&atilde;o, o sistema vem sofrendo mudan&ccedil;as para que os produtores se adaptem &agrave;s regras. Em janeiro de 2006 entra em vigor o novo modelo do Sistema Brasileiro de Identifica&ccedil;&atilde;o de Origem Bovina e Bubalina (Sisbov). Atualmente, 50 milh&otilde;es de animais est&atilde;o na base de dados, de um rebanho total de 190 milh&otilde;es de cabe&ccedil;as. </p>
      <p class="style10">A grande novidade do novo modelo, segundo M&aacute;rcio Portocarreo, &eacute; a exig&ecirc;ncia de certifica&ccedil;&atilde;o da propriedade e n&atilde;o mais do animal. &ldquo;Hoje podem conviver na mesma propriedade animais rastreados ou n&atilde;o. A partir do ano que vem quem quiser exportar para qualquer mercado, n&atilde;o somente para a Europa, tem que ter a propriedade certificada, com identifica&ccedil;&atilde;o e acompanhamento individual dos animais. </p>
      <p class="style10">&ldquo;As propriedades credenciadas ter&atilde;o que atender ao protocolo de boas pr&aacute;ticas que incluem quest&otilde;es tecnol&oacute;gicas, sanit&aacute;rias, ambientais, sociais e correspondentes a um alimento seguro&rdquo;, explicou o secret&aacute;rio. Ele alerta os produtores brasileiros de que a Europa est&aacute; ampliando o consumo de carne e diminuindo a produ&ccedil;&atilde;o e exige um sistema de rastreabilidade seguro e transparente. &ldquo;O Brasil talvez n&atilde;o esteja perdendo mercado de carne, mas sem a rastreabilidade pode deixar de ganhar&rdquo;. </p>
